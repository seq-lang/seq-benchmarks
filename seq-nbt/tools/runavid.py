import os, sys, subprocess, time
from multiprocessing import Pool
from tqdm import tqdm

def parse_avid(fn):
    cigar = ''
    la, lb = 0, 0
    prevOp, prevLen = '', 0
    ma, mi, id, nid = 0, 0, 0, 0
    with open(fn) as f:
        for l in f:
            l = l.strip().split()
            if l[0] == '|':
                op = 'I'
                lb += 1
                id += 1
            elif l[2] == '|':
                op = 'D'
                la += 1
                id += 1
            else:
                op = 'M'
                la += 1
                lb += 1
                if len(l) == 5:
                    ma += 1
                else:
                    mi += 1
            if op != prevOp:
                if prevOp != '':
                    cigar += f'{prevLen}{prevOp}'
                    if prevOp != 'M': nid += 1
                prevOp = op
                prevLen = 0
            prevLen += 1
        cigar += f'{prevLen}{prevOp}'
        if prevOp != 'M': nid += 1
    return cigar, ma, mi, id, nid

def parse_seq(fn):
    with open(fn) as f:
        l = f.readlines()
        cigar = l[0].strip()
        ma, mi, id, nid = list(map(int, l[1].strip().split()))
    return cigar, ma, mi, id, nid

with open(sys.argv[1]) as f:
    lines = list(f.readlines())
tool = sys.argv[3]

def align(inp):
    li, l = inp
    ref = 'data/genome.fa'
    ca, sa, ea, cb, sb, eb, xb, ma, mi, g, ind = l.strip().split()
    sa, ea, sb, eb, ma, mi, g, ind = map(int, [sa, ea, sb, eb, ma, mi, g, ind])

    xa = ''
    xb = '-i' if xb == '_' else ''
    try:
        r = os.system(f'samtools faidx {ref} {ca}:{int(sa)+1}-{ea} {xa} > {li}_1.fa')
        if r >> 8:
            return f'{li}\tfailed_samtools'
        r = os.system(f'samtools faidx {ref} {cb}:{int(sb)+1}-{eb} {xb} > {li}_2.fa')
        if r >> 8:
            return f'{li}\tfailed_samtools'

        if tool == 'avid':
            out = subprocess.check_output(f'`which time` -f"%E" tools/avid-linux -nm=both {li}_1.fa {li}_2.fa', shell=True, stderr=subprocess.STDOUT)
        else:
            out = subprocess.check_output(f'`which time` -f"%E" tools/seq/avid {li}_1.fa {li}_2.fa', shell=True, 
                # env={'LD_LIBRARY_PATH': 'seq/build:'},
                stderr=subprocess.STDOUT)
        assert('AVID is done.' in str(out))
        out = out.decode('utf-8').split('\n')
        t = out[-2].split(':')
        runtime = int(t[0])*60+float(t[1])

        fo = f'{li}_1.fa_{li}_2.fa.out'
        if tool == 'avid':
            cigar, ma, mi, id, nid = parse_avid(fo)
        else:
            cigar, ma, mi, id, nid = parse_seq(fo)
        return runtime, f'{li}\t{runtime}\t{cigar}\t{ma}\t{mi}\t{id}\t{nid}'
    except Exception as e:
        print(e)
        return f'{li}\tfailed_avid'
    finally:
        try:
            os.unlink(f'{li}_1.fa')
            os.unlink(f'{li}_2.fa')
            os.unlink(f'{li}_1.fa_{li}_2.fa.out')
            os.unlink(f'{li}_1.fa_{li}_2.fa.minfo')
            os.unlink(f'{li}_1.fa_{li}_2.fa.info')
        except Exception as e:
            pass
    return f'{li}\tfailed'

l = lines[:10]
res = tqdm(map(align, enumerate(l)), total=len(l))
